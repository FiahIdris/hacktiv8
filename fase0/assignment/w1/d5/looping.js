// FOR

console.log("LOOPING FOR PERTAMA");
var i = 1
for (var i=1; i<=20; ++i) {
    console.log(i+ " -  I love coding")
}

console.log("LOOPING FOR KEDUA")
for (var i=20; i>0; --i){
    console.log(i+" - I will become fullstack developer")
}

// WHILE
console.log("LOOPING WHILE PERTAMA")
var i = 2;
while (i<=20){
    console.log(i+ " - I love coding")
    i=i+2;
}

console.log("LOOPING WHILE KEDUA")
var i=20;
while (i>=2){
    console.log(i+ " - I will become fullstack developer")
    i=i-2;
}